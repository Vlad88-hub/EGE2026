# решение лесенкой
print('x y z w')
for x in range(2):
    for y in 0, 1:
        for z in (0, 1):
            for w in [0, 1]:
                f = not (w <= z) or (x <= y) or not x
                # Все строки истенны
                if f:
                    print(x, y, z, w)
                # Все строки ложные
                if not f:
                    print(x, y, z, w)
                # строки в перемешку
                print(x, y, z, w)

